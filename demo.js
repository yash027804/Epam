function addNumber(a, b) {
    return a + b;
}    

function subtractNumber(a, b) {
    return a - b;
}

function divideNumber(a, b) {
    return a / b;
}

function multiplyNumber(a, b) {
    return a * b;
}    

// Correct the export statement
export default { addNumber, subtractNumber, divideNumber, multiplyNumber };
